			<h3>Экономика</h3>
			Мы имеем свободную эмиссию. Любой, используя мощности своего компьютера, может в прямом смысле "напечатать" деньги.<br/>
			Таких пользователей обычно называют майнерами. Они "проводят" транзакции в системе и за это получают вознаграждение.<br/>
			Эмиссия ограничена, максимальный объем 20 миллионов монет. После, майнеры будут получать только комиссию за транзакции.<br/><br/>
			
			За счет выпуска новых монет, мы получаем инфляцию. За 2015, около 20.4%<br/>
			Это значение динамическое и может изменяться. Эмиссия уменьшается на 7% каждый год.<br/>
			Так же зависит от активности майнеров. Чем их больше - тем меньше эмиссия и наоборот.<br/><br/>
			
			Новые монеты майнеры могут оставить себе, продать на бирже или использовать в качестве средства оплаты.<br/>
