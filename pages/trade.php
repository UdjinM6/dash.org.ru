			<h3>Торговля на бирже</h3>
			
			Средний <a href="/pages/stats.php">объем торгов на биржах</a> от 50000 до 200000 долларов за день.<br/>
			Так как на рынке нет регулятора, спекулянты могут свободно пытаться манипулировать рынком.<br/>
			Например, когда есть положительные новости, они могут сыграть на этом и в разы увеличить цену.<br/>
			А потом когда их устроит прибыль - быстро продать свои монеты. Далее пытаться, например, выкупить их дешевле.<br/>
			В случае плохих новостей, они могут пытаться создать панику и купить дешевые монеты, чтобы потом, продать их дороже.<br/><br/>
			
			Присутствует высокая волатильность. Если вы любите биржу и умеете играть на курсе валют - возможно вас заинтересует этот рынок.<br/>
			На данный момент, основные биржи: <a href="https://www.cryptsy.com/" target="_blank">Cryptsy</a>, <a href="https://bittrex.com/" target="_blank">Bittrex</a>, <a href="https://cex.io/" target="_blank">CEX.IO</a>, <a href="https://bittrex.com/" target="_blank">Bitfinex</a>, <a href="https://bter.com/" target="_blank">BTER</a> и <a href="https://poloniex.com/" target="_blank">Poloniex</a>.<br/><br/>
			
			Обратите внимание, что фактически биржи никому не подчиняются. Возможно лишь законодательству той страны где находятся владелиц.<br/>
			Пополняя счет биржи, вы полностью передаете ей свои средства. И если с биржей, что-то случится - то получить их назад скорее всего будет достаточно сложно.<br/>
			Не рекомендуем использовать биржу как "кошелек" для DASH. Единственный надежный способ сохранить свои монеты - хранить их <a href="/pages/download.php">на своем зашифрованном кошельке</a>.<br/>
			В этом случае, никто кроме вас (без вашего wallet.dat + пароль) не сможет получить доступ к вашим средствам. Ответственность перед самим собой - плата за свободу.<br/><br/>
