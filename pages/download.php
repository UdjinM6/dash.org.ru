			Darksend - абсолютно независимая система, поскольку никто не может контролировать работу всей сети. Риск того, что что-то может работать неправильно - очень мал. Тем ни менее, это программное обеспечение всё ещё находится в стадии разработки. Это означает, что нет гарантий отсутствия рисков того, что система может дать сбой. Используя Dash, вы действуете на свой страх и риск - следуйте всем инструкциям в точности и используйте только те средства, которые вы можете позволить себе потерять в случае, если что-то пойдёт не так.<br/><br/>
			Darksend использует очень большой список ключей и в процессе своей работы задействует их достаточно интенсивно. Это означает, что вам следует делать резервные копии чаще, чем при использовании Кошельков других криптовалют, так как в процессе анонимизации Dash производится много автоматических транзакций в фоновом режиме. В целях безопасности вам требуется делать резервную копию каждый раз, когда увеличивается баланс ваших анонимизированных Dash.<br/><br/>
